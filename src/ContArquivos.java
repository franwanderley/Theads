public class ContArquivos {
   private Integer qtd;
   private String filename;

   public ContArquivos() {
      this.qtd = 0;
   }
   public Integer getQtd() {
      return qtd;
   }
   public String getFilename() {
      return filename;
   }
   public void setFilename(String filename) {
      this.filename = filename;
   }
   public void setQtd(Integer qtd) {
      this.qtd = qtd;
   }
   
}
